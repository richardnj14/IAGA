a = int(input("a: "))
b = int (input("b: "))

soma = a + b
sub = a - b
mult = a*b
div = a/b
divint = a//b
resto = a%b

print()
print(f"{a} + {b} = {soma}")
print(f"{a} - {b} = {sub}")
print(f"{a} * {b} = {mult}")
print(f"{a} / {b} = {div}")
print(f"{a} // {b} = {divint}")
print(f"{a} % {b} = {resto}")